typedef struct {
    const unsigned char *json;
    size_t position;
} cJSON_error;
static cJSON_error global_error = { NULL, 0 };

CJSON_PUBLIC(const char *) cJSON_GetErrorPtr(void)
{
    return (const char*) (global_error.json + global_error.position);
}

